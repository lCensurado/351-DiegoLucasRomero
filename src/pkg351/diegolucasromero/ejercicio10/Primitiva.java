     public void llenarArray(){
        int a=(int)(Math.random()*49);
        numeros[0]=a;
        for(int i=1;i<numeros.length;i++){            
            int b=(int)(Math.random()*49+1);
            if(b!=numeros[i-1]){
                numeros[i]=b;                
            }
        }
    }
     
